package proxy

import (
	"crypto/sha256"
	"encoding/hex"
	"encoding/json"
	"net/http"
	"strings"
)

func (be *Backend) serveLanguagesJSON(w http.ResponseWriter, req *http.Request) {
	req.ParseForm()
	var out map[string]map[string]string
	if lang := req.Form.Get("lang"); lang != "" {
		out = make(map[string]map[string]string)
		for _, key := range strings.Split(lang, ",") {
			if v, ok := languages[key]; ok {
				out[key] = v
			}
		}
	} else {
		out = languages
	}
	content, err := json.MarshalIndent(out, "", "  ")
	if err != nil {
		http.Error(w, err.Error(), http.StatusInternalServerError)
		return
	}
	sum := sha256.Sum256(content)
	etag := `"` + hex.EncodeToString(sum[:]) + `"`

	w.Header().Set("Content-Type", "application/json")
	w.Header().Set("Cache-Control", "public")
	w.Header().Set("Etag", etag)

	if e := req.Header.Get("If-None-Match"); e == etag {
		w.WriteHeader(http.StatusNotModified)
		return
	}
	w.WriteHeader(http.StatusOK)
	w.Write(content)
}

